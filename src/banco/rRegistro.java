    public void registrar(){
        nom = nombre.getText();
        id=identificacion.getText();
        password = String.copyValueOf(pass.getPassword());
        confPassword = String.copyValueOf(conPass.getPassword());
        
        try {
            DataOutputStream dos = new DataOutputStream(new FileOutputStream("Usuarios.dat", true));

            dos.writeUTF(nom);
            dos.writeUTF(id);
            dos.writeUTF(password);

            dos.flush();
            dos.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(rRegistro.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(rRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

