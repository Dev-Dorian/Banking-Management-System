    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        String p = String.copyValueOf(pass.getPassword());
        String p2 = String.copyValueOf(conPass.getPassword());

        if (nombre.getText().equals("") || identificacion.getText().equals("")
                || p.equals("") || p2.equals("")) {

            JOptionPane.showMessageDialog(null, "Hay espacios en blanco", "ERROR", JOptionPane.WARNING_MESSAGE);

        } else {
            if (p.equals(p2)) {
                registrar();
                Login entrada = new Login();
                entrada.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_ingresarActionPerformed

