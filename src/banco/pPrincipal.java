    public void guardar(){
        nombre=txtnombre.getText();
        direccion=txtdireccion.getText();
        telefono=txttelefono.getText();
        if (M.isSelected()) {
            sexo = "M";
        } else if (F.isSelected()) {
            sexo = "F";
        }
        civil=txtcivil.getText();
        tarjeta=cmbtarjeta.toString();
        edad=Integer.parseInt(txtedad.getText());
        id=Integer.parseInt(txtidentificacion.getText());
        cuenta=Integer.parseInt(txtcuenta.getText());
        
        try {
            DataOutputStream dos = new DataOutputStream(new FileOutputStream("DatosP.dat", true));

            dos.writeUTF(nombre);
            dos.writeUTF(sexo);
            dos.writeInt(edad);
            dos.writeUTF(direccion);
            dos.writeUTF(telefono);
            dos.writeInt(id);
            dos.writeUTF(civil);
            dos.writeUTF(tarjeta);
            dos.writeInt(cuenta);
            

            dos.flush();
            dos.close();

        } catch (FileNotFoundException ex) {

        } catch (IOException ex) {

        }
    }

