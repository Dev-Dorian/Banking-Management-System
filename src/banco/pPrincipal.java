    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if (txtnombre.getText().equals("") || txtdireccion.equals("") || txtedad.equals("")
                || txtidentificacion.equals("") || M.isSelected() == false && F.isSelected() == false 
                || txttelefono.equals("")|| txtcivil.equals("")|| txtcuenta.equals("")) {

            JOptionPane.showMessageDialog(null, "Hay espacios en blanco", "ERROR", JOptionPane.WARNING_MESSAGE);

        } else {
            guardar();
            txtdireccion.setText("");
            txtedad.setText("");
            txtidentificacion.setText("");
            txtnombre.setText("");
            txttelefono.setText("");
            txtcivil.setText("");
            cmbtarjeta.toString();
            txtcuenta.setText("");
            
            JOptionPane.showMessageDialog(null, "Guardado con exito", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

